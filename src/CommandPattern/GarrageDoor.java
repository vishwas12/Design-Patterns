package CommandPattern;

public class GarrageDoor {
	
	public void up(){
		System.out.println("garrage door opening up");
	}
	
	public void down(){
		System.out.println("garrage door closing down");
	}
	
	public void lightOn(){
		System.out.println("garrage door lights on");
	}
	
	public void lightOff(){
		System.out.println("garrage door lights off");
	}

}
